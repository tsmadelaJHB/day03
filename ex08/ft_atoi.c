int		ft_is_num(int c)
{
		if(c >= '0' && c <= '9')
			return 1;
		return 0;
}
int		ft_atoi(char *str)
{
	int i;
	int n;

	i = 0;
	n = 0;
	while((*str >= '\t' && *str <= '\r') || *str == 32)
		str++;
	if(*str == '-')
		n = -1;
	else
		n = 1;
	if(*str == '+' || *str == '-')
		str++;
	while(*str && ft_is_num(*str))
		i = (i * 10) + (*str++ - '0');
	return(i * n);
}
